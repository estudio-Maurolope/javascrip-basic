
//Asignacion simple = (Asigna un valor a una variable)
const nombre = "Juan";
const edad = 25;
console.log(`${nombre} tiene ${edad} años\n`)


//Asignación compuesta (+=, -=, =, /=): Realiza una operación matemática y asigna el resultado a una variable.
let numero = 10;
numero += 5; // numero = 15
console.log(`el numero es: ${numero}`);
numero -= 3; // numero = 12
console.log(`el numero es: ${numero}`);
numero *= 2; // numero = 24
console.log(`el numero es: ${numero}`);
numero /= 4; // numero = 6
console.log(`el numero es: ${numero}\n`);


//Asignación de incremento/decremento (++/--): Incrementa o decrementa el valor de una variable en 1.

let contador = 0;
contador++; // contador = 1
console.log(`contador incremento: ${contador}`);
contador--; // contador = 0
console.log(`contador decremento: ${contador}\n`);

//Calcular el total de una compra
const precioProducto = 10;
const cantidadProducto = 3;
const totalCompra = precioProducto * cantidadProducto;
console.log(`Total de la compra: ${totalCompra}\n`);

//Acumular puntos en un juego
let puntosJugador = 0;
puntosJugador += 100; // Por ganar un nivel
puntosJugador -= 20; // Por perder una vida
console.log(`Puntos del jugador: ${puntosJugador}\n`);


//Ajustar el volumen de un reproductor de música
let volumen = 50;
volumen++; // Aumentar volumen
volumen--; // Disminuir volumen
console.log(`Volumen actual: ${volumen}`);






