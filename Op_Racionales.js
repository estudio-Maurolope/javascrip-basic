//Igualdad (==): Evalúa si dos operandos son iguales en valor y tipo.
const resultadoIgualdad = 10 == "10"; // Output: true (ambos son 1
console.log(resultadoIgualdad);

//Desigualdad (!=): Evalúa si dos operandos son diferentes en valor o tipo.
const resultadoDesigualdad = 15 != "15"; // Output: true (diferent
console.log(resultadoDesigualdad);

//Menor que (<): Compara si un operando es menor que otro en valor numérico.
const resultadoMenorQue = 8 < 12; // Output: true
console.log(resultadoMenorQue);

//Mayor que (>): Compara si un operando es mayor que otro en valor numérico.
const resultadoMayorQue = 20 > 10; // Output: true
console.log(resultadoMayorQue);

//Menor o igual que (<=): Compara si un operando es menor o igual que otro en valor numérico.
const resultadoMenorOIgualQue = 15 <= 15; // Output: true
console.log(resultadoMenorOIgualQue);

//Mayor o igual que (>=): Compara si un operando es mayor o igual que otro en valor numérico.
const resultadoMayorOIgualQue = 22 >= 20; // Output: true
console.log(resultadoMayorOIgualQue);

